
public class Car {
  
  private static String myColor;
  private static String myMake;

  public void setColor(String s) {

    myColor = s;
  }

  public void setMake(String s) {

    myMake = s;
  }

  public String getColor() {

    return myColor;
  }

  public String getMake() {

    return myMake;
  }
}
